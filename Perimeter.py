"""
Computes the perimeter of a circle
"""
def circle(radius):
    perimeter = 3.14 * 2 * radius
    return perimeter

"""
Computes perimeter of a rectangle
"""
def rectangle(length, breadth):
    perimeter = 2 * (length + breadth)
    return perimeter


"""
Computes perimeter of a square
"""
def square(length):
    perimeter = 4 * length
    return perimeter